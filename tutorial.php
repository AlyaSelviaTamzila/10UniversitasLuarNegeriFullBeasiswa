            <h2>Tips Sukses Untuk Mendapatkan Beasiswa Ke Luar Negeri</h2>
            <img src="hl.png" style = "height : 350px; width : 800px;">
            <h2>Memperoleh beasiswa adalah salah satu cara terbaik untuk bisa melanjutkan kuliah di luar negeri.</h2>
                <p>Tapi sayangnya <b>jumlah beasiswa yang terbatas</b> berbanding terbalik dengan jumlah peminat beasiswa 
                   yang selalu bertambah tiap tahunnya. Tak ayal, <b>banyak sekali pelamar beasiswa yang harus kecewa 
                   saat gagal mendapat beasiswa yang diinginkan.</b> <b>Karena itu, kali ini kita akan berbagi tips sukses untuk 
                   mendapatkan beasiswa ke luar negeri:</b></p>
                <h3>1. Jangan Malu Bertanya atau Mencari Informasi</h3>
                <p>Bertanyalah kepada orang yang tepat.Kamu bisa mencari informasi di internet dan juga bertanya kepada 
                   pihak kampus (sekolah) seperti penasihat akademik, serta professor, dosen ataupun guru. Jika di kampus 
                   atau sekolahmu saat ini ada bagian yang menangani tentang studi di luar negeri maka tidak ada yang salah 
                   dengan bertanya langsung kepada mereka.</p>

                <h3>2. Buat Daftar Beasiswa yang Kamu Minati</h3>
                <p>Penting untuk membuat daftar beasiswa yang akan kamu apply.Jangan ragu untuk mencari tahu mulai dari 
                   jenis beasiswa, kualifikasi yang dibutuhkan, program studi yang ditawarkan hingga deadline beasiswa itu 
                   sendiri.Pada tips kedua ini kamu sebaiknya membuat spreadsheet untuk mempermudah pencarian dan 
                   pengklasifikasian beasiswa. Beberapa poin yang perlu kamu masukkan ke dalam spreadsheet seperti nama 
                   universitas, negara, bahasa yang digunakan, program studi dan lain sebagainya.Buat daftar beasiswa sebanyak 
                   mungkin dan apply, apply, apply lagi sampai kamu berhasil.</p>

                <h3>3. Pahami Visi dan Misi Organisasi</h3>
                <p>Sebelum mulai melamar beasiswa, akan lebih baik jika kamu memahami organisasi penyedia beasiswa itu sendiri.
                   Pahami seperti apa visi dan misi mereka. Cari tahu juga hal-hal apa yang sepertinya menjadi concern organisasi. 
                   Identifikasi semua informasi yang kamu dapat tentang organisasi dan buat aplikasimu sesuai dengan kebutuhan mereka.
                   Selain itu penting juga bagi kamu untuk tahu bagaimana cara mereka untuk mengevaluasi para kandidat pelamar beasiswa lho.</p>

                <h3>4. Pilih Negara Dengan Pelamar Lebih Sedikit</h3>
                <p>Tips keempat ini sebenarnya cukup ampuh untuk bisa memperoleh beasiswa ke luar negeri.Jangan hanya memilih negara 
                   dengan peminat beasiswa yang membludak.Misalkan saja Inggris diminati oleh 500 pelamar maka kesempatanmu untuk 
                   bisa mendapat beasiswa berada di angka 1:500. Buat daftar negara-negara yang peminatnya lebih sedikit dan cari 
                   tahu lebih dalam mengenai beasiswa-beasiswa yang disediakan oleh negara tersebut.</p>

                <h3>5. Buat Motivation Letter yang Anti-Mainstream</h3>
                <p>Motivation Letter adalah salah satu persyaratan yang selalu dibutuhkan dalam aplikasi beasiswa.Sebisa mungkin 
                   hindari kata-kata mainstream seperti “Saya ingin mempelajari budaya baru”, “Saya sangat tertarik untuk belajar 
                   Bahasa Jerman” dan lain sebagainya.Sebaliknya kamu bisa menceritakan beberapa fakta seperti misalnya kamu jatuh 
                   cinta dengan film-film Perancis atau kamu pernah memiliki teman SMA dari Polandia yang ikut pertukaran pelajar 
                   dan beberapa fakta-fakta pendukung lain yang membuktikan bahwa kamu memang punya keinginan dan dasar yang kuat 
                   untuk melanjutkan kuliah di negara tersebut.Intinya buat essay yang menunjukkan bahwa essay ini memang hanya milik 
                   kamu.</p>

                <h3>6. Masukkan Lamaran Kamu Lebih Awal</h3>
                <p>Salah satu kebiasaan buruk saat melamar beasiswa adalah memasukkan lamaran di waktu-waktu terakhir.Hindari kebiasaan 
                   buruk ini dan cobalah untuk memasukkan lamaran di awal. Memang tidak ada jaminan kamu akan mendapat beasiswa jika 
                   mengirim aplikasi di awal.Tapi setidaknya secara logika kamu harus sadar bahwa ada orang lain di pihak penyedia beasiswa 
                   yang akan menerima dan memeriksa aplikasimu.Orang-orang yang diberi tanggung jawab ini biasanya akan lebih jeli di awal 
                   aplikasi beasiswa masuk. Saat sudah mendekati deadline maka mayoritas dari mereka hanya akan melihat aplikasi secara sekilas 
                   dan sangat mungkin bahwa poin-poin plus di aplikasimu lolos dari penglihatan mereka. Jadi buat aplikasi sebaik mungkin dan 
                   submit di awal ya.</p>
